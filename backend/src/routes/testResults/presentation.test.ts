describe('escapeHtml', () => {
  it('escapes HTML-sensitive characters', () => {
    expect(escapeHtml(`A & B <Co> "x" 'y'`)).toBe('A &amp; B &lt;Co&gt; &quot;x&quot; &#39;y&#39;');
  });

  it('returns the fallback for null, undefined, and empty string', () => {
    expect(escapeHtml(null)).toBe('N/A');
    expect(escapeHtml(undefined)).toBe('N/A');
    expect(escapeHtml('')).toBe('N/A');
    expect(escapeHtml(null, '')).toBe('');
    expect(escapeHtml('', '(none)')).toBe('(none)');
  });

  it('stringifies non-empty non-string values (including 0)', () => {
    expect(escapeHtml(0)).toBe('0');
    expect(escapeHtml(123)).toBe('123');
  });
});

