describe('buildVerificationViewData', () => {
  it('computes pass / fail / unknown specification status', () => {
    const status = (overrides: Partial<VerificationViewSource>) =>
      buildVerificationViewData(verificationViewSource(overrides), { canVerify: false })
        .specification.calculatedStatus;

    expect(status({ resultValue: 98, specificationMin: 95, specificationMax: 100 })).toBe('pass');
    expect(status({ resultValue: 120, specificationMin: 95, specificationMax: 100 })).toBe('fail');
    expect(status({ resultValue: null })).toBe('unknown');
    expect(status({ resultValue: 90, specificationMin: 95, specificationMax: null })).toBe('fail');
    expect(status({ resultValue: 99, specificationMin: 95, specificationMax: null })).toBe('pass');
    expect(status({ resultValue: 50, specificationMin: null, specificationMax: 100 })).toBe('pass');
    expect(status({ resultValue: 120, specificationMin: null, specificationMax: 100 })).toBe(
      'fail',
    );
    expect(status({ resultValue: 98, specificationMin: null, specificationMax: null })).toBe(
      'unknown',
    );
  });

  it('passes through canVerify and derives needsVerification from status', () => {
    expect(buildVerificationViewData(verificationViewSource(), { canVerify: true }).canVerify).toBe(
      true,
    );
    expect(
      buildVerificationViewData(verificationViewSource({ status: 'entered' }), { canVerify: false })
        .needsVerification,
    ).toBe(true);
    expect(
      buildVerificationViewData(verificationViewSource({ status: 'verified' }), {
        canVerify: false,
      }).needsVerification,
    ).toBe(false);
  });

  it('maps the certificate document with an isPdf flag', () => {
    const noDoc = buildVerificationViewData(verificationViewSource(), { canVerify: false });
    expect(noDoc.document).toBeNull();

    const uploadedAt = new Date('2026-05-14T02:00:00.000Z');
    const pdf = buildVerificationViewData(
      verificationViewSource({
        certificateDoc: {
          id: 'doc-1',
          filename: 'cert.pdf',
          fileUrl: 'https://files/cert.pdf',
          mimeType: 'application/pdf',
          uploadedAt,
        },
      }),
      { canVerify: false },
    );
    expect(pdf.document).toEqual({
      id: 'doc-1',
      filename: 'cert.pdf',
      fileUrl: 'https://files/cert.pdf',
      mimeType: 'application/pdf',
      uploadedAt,
      isPdf: true,
    });

    const png = buildVerificationViewData(
      verificationViewSource({
        certificateDoc: {
          id: 'doc-2',
          filename: 'cert.png',
          fileUrl: 'https://files/cert.png',
          mimeType: 'image/png',
          uploadedAt,
        },
      }),
      { canVerify: false },
    );
    expect(png.document?.isPdf).toBe(false);
  });

  it('resolves the standard specification reference by normalized test type', () => {
    expect(
      buildVerificationViewData(verificationViewSource({ testType: 'compaction' }), {
        canVerify: false,
      }).specification.standardReference,
    ).toBe('TMR MRTS04 / AS 1289.5.4.1');
    // "Compaction Test" normalizes to "compaction_test", which is not a known key
    expect(
      buildVerificationViewData(verificationViewSource({ testType: 'Compaction Test' }), {
        canVerify: false,
      }).specification.standardReference,
    ).toBeNull();
  });

  it('returns an empty confidence summary when the result is not AI-extracted', () => {
    expect(
      buildVerificationViewData(verificationViewSource({ aiExtracted: false }), {
        canVerify: false,
      }).confidenceHighlights,
    ).toEqual({ hasLowConfidence: false, lowConfidenceFields: [], fieldStatus: {} });
  });

  it('buckets AI confidence into high / medium / low and flags low fields for review', () => {
    const result = buildVerificationViewData(
      verificationViewSource({
        aiExtracted: true,
        aiConfidence: JSON.stringify({ testType: 0.95, sampleDate: 0.85, resultValue: 0.5 }),
      }),
      { canVerify: false },
    );

    expect(result.confidenceHighlights).toEqual({
      hasLowConfidence: true,
      lowConfidenceFields: ['resultValue'],
      fieldStatus: {
        testType: { confidence: 0.95, status: 'high', needsReview: false },
        sampleDate: { confidence: 0.85, status: 'medium', needsReview: false },
        resultValue: { confidence: 0.5, status: 'low', needsReview: true },
      },
      thresholds: { low: 0.8, medium: 0.9 },
      reviewMessage:
        '1 field(s) have low AI confidence and require manual verification: resultValue',
    });
  });

  it('parses aiConfidence into extractedData when present', () => {
    const result = buildVerificationViewData(
      verificationViewSource({
        aiExtracted: true,
        aiConfidence: JSON.stringify({ testType: 0.95 }),
      }),
      { canVerify: false },
    );
    expect(result.extractedData.aiExtracted).toBe(true);
    expect(result.extractedData.aiConfidence).toEqual({ testType: 0.95 });
  });
});

